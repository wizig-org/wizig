    var out = std.ArrayList(u8).empty;
    errdefer out.deinit(allocator);

    try out.appendSlice(
        allocator,
        "// Code generated by `wizig plugin sync`. DO NOT EDIT.\n" ++
            "package dev.wizig\n\n" ++
            "data class PluginDescriptor(\n" ++
            "    val id: String,\n" ++
            "    val version: String,\n" ++
            "    val apiVersion: UInt,\n" ++
            "    val manifestPath: String,\n" ++
            "    val capabilities: List<String>,\n" ++
            "    val iosSpm: List<String>,\n" ++
            "    val androidMaven: List<String>\n" ++
            ")\n\n" ++
            "object GeneratedPluginRegistrant {\n" ++
            "    val plugins: List<PluginDescriptor> = listOf(\n",
    );

    for (records) |record| {
        try out.appendSlice(allocator, "        PluginDescriptor(\n            id = ");
        try appendQuoted(&out, allocator, record.manifest.id);
        try out.appendSlice(allocator, ",\n            version = ");
        try appendQuoted(&out, allocator, record.manifest.version);
        try appendFmt(&out, allocator, ",\n            apiVersion = {d}u,\n", .{record.manifest.api_version});
        try out.appendSlice(allocator, "            manifestPath = ");
        try appendQuoted(&out, allocator, record.manifest_path);
