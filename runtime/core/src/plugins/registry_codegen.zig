//! Runtime-packaged plugin registry collection and registrant generators.
const std = @import("std");
const PluginManifest = @import("manifest.zig").PluginManifest;

/// Plugin file path paired with parsed manifest contents.
pub const PluginRecord = struct {
    manifest_path: []u8,
    manifest: PluginManifest,

    /// Releases owned path/manifest data.
    pub fn deinit(self: *PluginRecord, allocator: std.mem.Allocator) void {
        allocator.free(self.manifest_path);
        self.manifest.deinit(allocator);
        self.* = undefined;
    }
};

/// In-memory registry of discovered plugins.
pub const Registry = struct {
    records: []PluginRecord,

    /// Releases all registry records and their owned allocations.
    pub fn deinit(self: *Registry, allocator: std.mem.Allocator) void {
        for (self.records) |*record| {
            record.deinit(allocator);
        }
        allocator.free(self.records);
        self.* = undefined;
    }
};

/// Collects plugin manifests from the given `plugins_dir`.
pub fn collectFromDir(
    allocator: std.mem.Allocator,
    io: std.Io,
    root_path: []const u8,
) !Registry {
    var root_dir = try std.Io.Dir.cwd().openDir(io, root_path, .{ .iterate = true });
    defer root_dir.close(io);

    var walker = try root_dir.walk(allocator);
    defer walker.deinit();

    var records = std.ArrayList(PluginRecord).empty;
    errdefer deinitRecordList(allocator, &records);

    while (try walker.next(io)) |entry| {
        if (entry.kind != .file) continue;
        if (!std.mem.eql(u8, entry.basename, "wizig-plugin.toml")) continue;

        const manifest_text = try root_dir.readFileAlloc(io, entry.path, allocator, .limited(1024 * 1024));
        defer allocator.free(manifest_text);

        var manifest = try PluginManifest.parse(allocator, manifest_text);
        errdefer manifest.deinit(allocator);

        const manifest_path = try combineManifestPath(allocator, root_path, entry.path);
        errdefer allocator.free(manifest_path);

        try records.append(allocator, .{
            .manifest_path = manifest_path,
            .manifest = manifest,
        });
    }

    const records_slice = try records.toOwnedSlice(allocator);
    errdefer {
        for (records_slice) |*record| {
            record.deinit(allocator);
        }
        allocator.free(records_slice);
    }

    try sortAndValidate(records_slice);

    return .{ .records = records_slice };
}

/// Renders deterministic lockfile text for all plugin records.
pub fn renderLockfile(allocator: std.mem.Allocator, records: []const PluginRecord) ![]u8 {
    var out = std.ArrayList(u8).empty;
    errdefer out.deinit(allocator);

    try out.appendSlice(allocator, "# Code generated by `wizig plugin sync`. DO NOT EDIT.\n");
    try out.appendSlice(allocator, "format_version = 1\n\n");

    for (records) |record| {
        try out.appendSlice(allocator, "[[plugins]]\n");
        try out.appendSlice(allocator, "id = ");
        try appendQuoted(&out, allocator, record.manifest.id);
        try out.appendSlice(allocator, "\nversion = ");
        try appendQuoted(&out, allocator, record.manifest.version);
        try appendFmt(&out, allocator, "\napi_version = {d}\n", .{record.manifest.api_version});
        try out.appendSlice(allocator, "manifest_path = ");
        try appendQuoted(&out, allocator, record.manifest_path);
        try out.appendSlice(allocator, "\ncapabilities = ");
        try appendBracketedStringArray(&out, allocator, record.manifest.capabilities);
        try out.appendSlice(allocator, "\nios_spm = ");
        try appendBracketedStringArray(&out, allocator, record.manifest.ios_spm);
        try out.appendSlice(allocator, "\nandroid_maven = ");
        try appendBracketedStringArray(&out, allocator, record.manifest.android_maven);
        try out.appendSlice(allocator, "\n\n");
    }

    return out.toOwnedSlice(allocator);
}

/// Renders Zig registrant source from discovered plugins.
pub fn renderZigRegistrant(allocator: std.mem.Allocator, records: []const PluginRecord) ![]u8 {
    var out = std.ArrayList(u8).empty;
    errdefer out.deinit(allocator);

    try out.appendSlice(
        allocator,
        "// Code generated by `wizig plugin sync`. DO NOT EDIT.\n\n" ++
            "pub const RegisteredPlugin = struct {\n" ++
            "    id: []const u8,\n" ++
            "    version: []const u8,\n" ++
            "    api_version: u32,\n" ++
            "    manifest_path: []const u8,\n" ++
            "    capabilities: []const []const u8,\n" ++
            "    ios_spm: []const []const u8,\n" ++
            "    android_maven: []const []const u8,\n" ++
            "};\n\n" ++
            "pub const plugins: []const RegisteredPlugin = &.{\n",
    );

    for (records) |record| {
        try out.appendSlice(allocator, "    .{\n");
        try out.appendSlice(allocator, "        .id = ");
        try appendQuoted(&out, allocator, record.manifest.id);
        try out.appendSlice(allocator, ",\n        .version = ");
        try appendQuoted(&out, allocator, record.manifest.version);
        try appendFmt(&out, allocator, ",\n        .api_version = {d},\n", .{record.manifest.api_version});
        try out.appendSlice(allocator, "        .manifest_path = ");
        try appendQuoted(&out, allocator, record.manifest_path);
        try out.appendSlice(allocator, ",\n        .capabilities = ");
        try appendZigStringSlice(&out, allocator, record.manifest.capabilities);
        try out.appendSlice(allocator, ",\n        .ios_spm = ");
        try appendZigStringSlice(&out, allocator, record.manifest.ios_spm);
        try out.appendSlice(allocator, ",\n        .android_maven = ");
        try appendZigStringSlice(&out, allocator, record.manifest.android_maven);
        try out.appendSlice(allocator, ",\n    },\n");
    }

    try out.appendSlice(allocator, "};\n");
    return out.toOwnedSlice(allocator);
}

